def is_persistent(self):
    return True

def Persistent(cls):
    cls.is_persistent = is_persistent
    return cls

def PersistentTime(save_time=None):
    def _persistent_time(cls):
        cls.is_persistent = is_persistent
        if save_time is not None:
            cls.get_persistent_time = lambda self: save_time
        return cls
    return _persistent_time

def FieldsList(fields):
    def _init_from_dict(func):
        func.fieldslist = fields
        return func
    return _init_from_dict
